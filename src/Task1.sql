CREATE DATABASE;

CREATE TABLE VISITORS;
id INTEGER PRIMARY KEY;
fio VARCHAR(50);
date_of_birth VARCHAR(50);
gender TEXT;
phone_number VARCHAR(50);

CREATE TABLE SECTION_TYPE;
id INTEGER PRIMARY KEY;
name VARCHAR(50);

CREATE TABLE TRAINERS;
id INTEGER PRIMARY KEY;
fio VARCHAR(50);
gender TEXT;
SECTION_TYPE_id INTEGER FOREIGN KEY;

CREATE TABLE VISITS;
visitors_id INTEGER FOREIGN KEY;
visit_time TIMESTAMP;
trainer_id INTEGER FOREIGN KEY;

    SELECT * FROM SECTION_TYPE JOIN TRAINERS ON SECTION_TYPE.id = TRAINERS.SECTION_TYPE_id;
    SELECT * FROM VISITORS JOIN VISITS ON VISITORS.id = VISITS.visitors_id;
    SELECT * FROM TRAINERS JOIN VISITS ON TRAINERS.id = VISITS.trainers_id;

